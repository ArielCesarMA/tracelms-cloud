// ── HELPERS ──────────────────────────────────────────────────────────────────
function sheet(data, cols) {
  const ws = XLSX.utils.aoa_to_sheet(data);
  ws["!cols"] = cols.map(w => ({ wch: w }));
  return ws;
}

function addSheet(name, data, cols) {
  const ws = sheet(data, cols);
  XLSX.utils.book_append_sheet(wb, ws, name);
}

